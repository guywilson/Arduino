/*
** From ADC inputs linked to a Texas Instruments LM35CAZ temperature sensor
** with a range of -40C to 110C. The +ve and -ve ranges from the sensor go
** to seperate analogue inputs. The sensor output before amplification is
** -400mV to +1150mV
*/

/*
** For +ve temperatures (0C to 60C), after amplification maps to 0 - 5V 
** which means each digital value (0 - 1023) equates to 0.0586 degrees.
**
** For -ve temperatures, we us the same scaling, so (0 to -40C) after
** amplification maps to 0 - 2V giving a digital range of 0 - 683.
*/
